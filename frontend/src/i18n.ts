const fallback = translations['ko'];

export function getT(lang: string) {
  const msgs = translations[lang] ?? fallback;
  return function t(key: keyof Msgs, arg?: string | number): string {
    const val = msgs[key] ?? fallback[key];
    if (typeof val === 'function') return (val as (a: string | number) => string)(arg!);
    return val as string;
  };
}
